#include <cstdlib>
#include "Scene.h"

//-------------------------------------------------------------------------------------------------------------------------
/// @file main.cpp
/// @brief Main function
//-------------------------------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------------------------------
int main()
{
  //create new instance of Scene
  Scene world("Particle Simulation",0,0,720,576);
  world.makeCurrent();
  //start main loop
  while(!world.m_quit) //exit when prompted
  {
    //take user input and draw
    world.handleInput();
    world.draw();
  }
  return EXIT_SUCCESS;
}
